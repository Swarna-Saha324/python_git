set_a = {1,2,3}
set_b = {3, 4, 5}
# Union of two sets
set_union = set_a.union(set_b)  # or set_a | set_b
print("Union of set_a and set_b:", set_union)
# Intersection of two sets
set_intersection = set_a.intersection(set_b)  # or set_a & set_b
print("Intersection of set_a and set_b:", set_intersection)
# Difference of two sets
set_difference = set_a.difference(set_b)  # or set_a - set_b
print("Difference of set_a and set_b:", set_difference)
# Symmetric difference of two sets
set_symmetric_difference = set_a.symmetric_difference(set_b)  # or set_a ^ set_b
print("Symmetric difference of set_a and set_b:", set_symmetric_difference)
# Checking subset and superset
print("Is set_a a subset of set_b?", set_a.issubset(set_b))
print("Is set_b a superset of set_a?", set_b.issuperset(set_a))
print("Set Operations Completed.")
